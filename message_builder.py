def _e(text: str) -> str:
    return html.escape(str(text))


def build_message(product: dict, index: int = 1, total: int = 1) -> str:
    category  = product.get("category", "")
    hooks     = HOOKS_BY_CATEGORY.get(category, HOOKS_DEFAULT)
    openers   = OPENERS_BY_CATEGORY.get(category, OPENERS_DEFAULT)
    hook      = random.choice(hooks)
    opener    = random.choice(openers)
    cat_emoji = CATEGORY_EMOJIS.get(category, "⭐")

    name     = _e(product.get("name", "מוצר מיוחד"))
    link     = product["aliexpress_link"]
    features = product.get("features", [])
    orders   = product.get("orders")
    rating   = product.get("rating")

    # Feature bullets — max 6 lines, clean and short
    feature_lines = ""
    if features:
        bullets = []
        for f in features[:6]:
            f = f.strip().replace("/", "").replace("\\", "").replace("|", "")
            f = " ".join(f.split())
            f = f.strip(" :-–•·")
            if not f:
                continue
            # Keep short enough to not wrap in Telegram (Hebrew ~38 chars max)
            if len(f) > 38:
                f = f[:35].rsplit(" ", 1)[0] + "..."
            bullets.append(f"✅ {_e(f)}")
        feature_lines = "\n".join(bullets)

    # Social proof line — only show real data, never invent numbers
    if orders and rating:
        proof_line = f"✅ מעל {_e(orders)} רכישות | דירוג {_e(rating)} ⭐"
    elif orders:
        proof_line = f"✅ מעל {_e(orders)} רכישות"
    elif rating:
        proof_line = f"✅ דירוג {_e(rating)} ⭐"
    else:
        proof_line = None

    lines = [
        hook,
        "",
        f"{cat_emoji} <b>{name}</b>",
        "",
        opener,
        "",
    ]

    if feature_lines:
        lines += [feature_lines, ""]

    if proof_line:
        lines += [proof_line, ""]

    lines.append(f"לרכישה ➡️ {link}")

    return "\n".join(lines)


